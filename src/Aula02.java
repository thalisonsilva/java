public class Aula02 {
    public static void main(String[] args) {
            // Declaração de variáveis
            int idade = 25; // Tipo inteiro
            double altura = 1.75; // Tipo ponto flutuante
            char inicial = 'J'; // Tipo caractere
            boolean estudante = true; // Tipo booleano
            String nome = "João"; // Tipo String (sequência de caracteres)

            // Uso das variáveis
            System.out.println("Nome: " + nome);
            System.out.println("Idade: " + idade);
            System.out.println("Altura: " + altura + " metros");
            System.out.println("Inicial do nome: " + inicial);
            System.out.println("É estudante? " + estudante);
            System.out.println("É estudante? " + estudante);
    }
}
